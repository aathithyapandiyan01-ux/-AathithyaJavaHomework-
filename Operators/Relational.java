class Relational
{
 public static void main (String [] args )
 {
  int X = 10;
  int Y = 15;

  System.out.println ("X==Y :" +(X==Y) );
  System.out.println ("X<=Y :" +(X<=Y) );
  System.out.println ("X>=Y :" +(X>=Y) );
  System.out.println ("X>Y :" +(X>Y) );
  System.out.println ("X<Y :" +(X<Y) );
  System.out.println ("X!=Y :" +(X!=Y) );

 }

}