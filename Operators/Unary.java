class Unary
{
 public static void main (String []args )
 {
  int A = 5;

  System.out.println (" Original : " + (A));

  A++;
  System.out.println (" After A++ : " + (A));

  ++A;
  System.out.println (" After ++A : " + (A));

  A--;
  System.out.println (" After A-- : " + (A));


  --A;
  System.out.println (" After --A : " + (A));

 }

}